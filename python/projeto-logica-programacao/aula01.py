#a função print é usada para mostrar uma mensagem
#ao usuário
print("Boa noite!")

#imprimindo numero inteiro
print(1)

#imprimindo numero float
print(4.5)

#somando: para somar, usamos o +
print("5 + 2 =")#exibe o texto 5 + 2 =
print(5+2)#exibe o resultado de 5 + 2, ou seja, 7

#subtraindo: para subtrair, usamos o -
print("10 - 1 =")#exibe o texto 10 - 1 =
print(10 - 1)#exibe o resultado de 10 - 1, ou seja, 9

#multiplicando: para multiplicar, usamos o * 
print("2 * 8 =")#exibe o texto 2 * 8 =
print(2 * 8)#exibe o resultado de 2 * 8, ou seja, 16

#dividindo: para dividir, usamos a /
print("8 / 2 =")#exibe o texto 8 / 2 =
print(8 / 2)#exibe o resultado de 8 / 2, ou seja, 4.0
#toda a divisão feita no python, resulta em um número
#real, independente dos divisores serem inteiros ou reais


#para divisão trazendo somente a parte inteira, utilizamos
# a //
print("15 // 2 = ")
print(15 // 2)


#para olhar para o resto da divisão, usamos o módulo que 
#no python é representado pelo simbolo %
print("7 % 2 =")
print(7 % 2)



