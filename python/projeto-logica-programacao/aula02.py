#operadores relacionais 

#operador maior (>)
print("5 > 2")
print(5 > 2)

#operador menor (<)
print("5 < 2")
print(5 < 2)

#operador maior ou igual (>=)
print("5 >= 8")
print(5 >= 8)

#operador menor ou igual (<=)
print("5 <= 8")
print(5 <= 8)

#operador igual/comparação (==)
print("10 == 10")
print(10 == 10) 

#operador diferente (!=)
print("10 != 10")
print(10 != 10)


print("Sim == Sim")
print("Sim" == "SiM")

print("Sim != Não")
print("Sim" != "Não")
